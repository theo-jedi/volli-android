package com.theost.volli.models;

public class Event {

    private int day;
    private int hours;
    private int minutes;
    private int month;
    private int year;

    private String text;
    private String title;

    public int getDay() {
        return day;
    }

    public int getHours() {
        return hours;
    }

    public int getMinutes() {
        return minutes;
    }

    public int getMonth() {
        return month;
    }

    public int getYear() {
        return year;
    }

    public String getText() {
        return text;
    }

    public String getTitle() {
        return title;
    }

}
